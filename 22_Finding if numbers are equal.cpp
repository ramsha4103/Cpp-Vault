//Program that finds out if two numbers are equal 
#include<iostream>
using namespace std;
int main()
{
	int a,b;
	cout<<"Enter the first number ";
	cin>>a;
	cout<<"Enter the second number ";
	cin>>b;
	if(a==b)
	cout<<"The numbers are equal";
	if(a!=b)
	cout<<"The numbers are not equal";
	return(0);
}

